# LISTAS
#

dias =["lunes", "martes", "miercoles", "jueves", "viernes"]

#imprimir uno o varios dias
print(dias[0])
print(dias[4])
print(dias[1:4])

#agregar valores a la lista

dias.append("sabado")
dias.append("domingo")

#eliminar valores de la lista

dias.pop() #elimina el ultimo elemento
dias.pop(3) #elimina el elemento en la posicion 0
print(dias)

#modificar un dato

dias[3] = "jueves"
dias[4] = "viernes"

print(dias)

